public class printNumbers
{
    public static void main(String[] args)
    {
        for (int i = 1; i <= 10; i++)
        // for identifica il tipo di ciclo che si verifica per un numero predeterminato di volte
        // quante volte? Quanto è il valore che assume i
        // i inizialmente è 1
        // con il punto e virgola posso assegnare un altro valore ad i in questo caso 10 che rappresenta il numero massimo di cicli
        // ad ogni ciclo i aumenta incrementalmente di 1 unità (perchè c'è ++)
        {
            System.out.println(i);
        }
    }
}